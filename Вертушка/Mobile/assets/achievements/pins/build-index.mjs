/**
 * Сканирует *.svg в этой папке и генерирует pins-index.ts с
 * { [slug_lower]: '<svg>...</svg>' }.
 *
 * Запуск:
 *   node Mobile/assets/achievements/pins/build-index.mjs
 *
 * Запускать после добавления/удаления SVG-пинов. Сгенерированный файл —
 * коммитим (нет runtime fs-доступа в RN, всё инлайнится в bundle).
 */
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));

const entries = readdirSync(HERE)
  .filter((f) => f.toLowerCase().endsWith('.svg'))
  .sort();

const out = ['// AUTO-GENERATED by build-index.mjs — do not edit by hand.', ''];
out.push('export const PIN_SVGS: Record<string, string> = {');
for (const f of entries) {
  const slug = f.replace(/\.svg$/i, '').toLowerCase();
  const xml = readFileSync(join(HERE, f), 'utf8')
    .replace(/\r?\n\s*/g, ' ')
    .replace(/  +/g, ' ')
    .trim();
  // Use JSON.stringify to escape backslashes / quotes safely.
  out.push(`  ${JSON.stringify(slug)}: ${JSON.stringify(xml)},`);
}
out.push('};');
out.push('');
out.push(`export const PIN_SLUGS = Object.freeze(${JSON.stringify(entries.map((f) => f.replace(/\.svg$/i, '').toLowerCase()))}) as readonly string[];`);
out.push('');

writeFileSync(join(HERE, 'pins-index.ts'), out.join('\n'));
console.log(`Generated pins-index.ts with ${entries.length} pins.`);
