# –ü–æ–ø—ã—Ç–∞—Ç—å—Å—è –ø—Ä–∏–º–µ–Ω–∏—Ç—å —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –æ–±—Ä–∞—Ç–Ω–æ (–µ—Å–ª–∏ –±—ã–ª–∏)
if [ "$STASHED" = true ]; then
    echo "üîÑ –ü—ã—Ç–∞—é—Å—å –ø—Ä–∏–º–µ–Ω–∏—Ç—å —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è..."
    if git stash pop 2>/dev/null; then
        echo "‚úÖ –õ–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è —É—Å–ø–µ—à–Ω–æ –ø—Ä–∏–º–µ–Ω–µ–Ω—ã"
    else
        echo "‚ö†Ô∏è  –ù–µ —É–¥–∞–ª–æ—Å—å –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø—Ä–∏–º–µ–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è (–≤–æ–∑–º–æ–∂–Ω—ã –∫–æ–Ω—Ñ–ª–∏–∫—Ç—ã)"
        echo "   –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ 'git stash list' –∏ 'git stash show' –¥–ª—è –ø—Ä–æ—Å–º–æ—Ç—Ä–∞"
    fi
fi

# –ü–µ—Ä–µ–π—Ç–∏ –≤ Backend
cd –í–µ—Ä—Ç—É—à–∫–∞/Backend

# –°–±–æ—Ä–∫–∞ –Ω–æ–≤–æ–≥–æ –æ–±—Ä–∞–∑–∞
echo "üî® –°–æ–±–∏—Ä–∞—é Docker –æ–±—Ä–∞–∑..."
docker compose -f docker-compose.prod.yml build api

# –ó–∞–ø—É—Å–∫ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤
echo "üê≥ –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞—é –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã..."
docker compose -f docker-compose.prod.yml up -d

# –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ –º–∏–≥—Ä–∞—Ü–∏–π
echo "üìä –ü—Ä–∏–º–µ–Ω—è—é –º–∏–≥—Ä–∞—Ü–∏–∏ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö..."
docker compose -f docker-compose.prod.yml exec -T api alembic upgrade head

# –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä—ã—Ö –æ–±—Ä–∞–∑–æ–≤
echo "üßπ –û—á–∏—â–∞—é —Å—Ç–∞—Ä—ã–µ Docker –æ–±—Ä–∞–∑—ã..."
