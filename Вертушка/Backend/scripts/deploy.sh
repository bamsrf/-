#!/bin/bash
# ===========================================
# –°–∫—Ä–∏–ø—Ç –¥–µ–ø–ª–æ—è –í–µ—Ä—Ç—É—à–∫–∞ API
# ===========================================

set -e  # –û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –ø—Ä–∏ –æ—à–∏–±–∫–µ

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo -e "${YELLOW}üöÄ –ù–∞—á–∏–Ω–∞—é –¥–µ–ø–ª–æ–π –í–µ—Ä—Ç—É—à–∫–∞ API...${NC}"

# –ü–µ—Ä–µ–π—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –ø—Ä–æ–µ–∫—Ç–∞
cd ~/vertushka

# –°–æ—Ö—Ä–∞–Ω–∏—Ç—å –ª–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è –ø–µ—Ä–µ–¥ pull (–µ—Å–ª–∏ –µ—Å—Ç—å)
if ! git diff-index --quiet HEAD --; then
    echo "üíæ –°–æ—Ö—Ä–∞–Ω—è—é –ª–æ–∫–∞–ª—å–Ω—ã–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è..."
    git stash push -m "Auto-stash before deploy $(date +%Y-%m-%d_%H:%M:%S)"
    STASHED=true
else
    STASHED=false
fi

# –ü–æ–ª—É—á–∏—Ç—å –ø–æ—Å–ª–µ–¥–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è
echo "üì• –ü–æ–ª—É—á–∞—é –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∏–∑ git..."
